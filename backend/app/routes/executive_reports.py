@router.put("/schedules/{schedule_id}")
def update_schedule(
    schedule_id: int,
    payload: ScheduleUpdate,
    db: Session = Depends(get_db),
    user = Depends(verify_role("manager"))
):
    schedule = db.query(ReportSchedule).filter(
        ReportSchedule.id == schedule_id
    ).first()

    if not schedule:
        raise HTTPException(status_code=404, detail="Schedule not found")

    if schedule.created_by != current_user.id:
        raise HTTPException(status_code=403, detail="Only the creator can update this schedule")

    if payload.frequency:
        schedule.frequency = payload.frequency
    if payload.day_of_week:
        schedule.day_of_week = payload.day_of_week
    if payload.day_of_month:
        schedule.day_of_month = payload.day_of_month
    if payload.is_active is not None:
        schedule.is_active = payload.is_active

    db.commit()
    db.refresh(schedule)

    return success_response(
        message="Schedule updated successfully!",
        data={
            "id": schedule.id,
            "report_type": schedule.report_type,
            "frequency": schedule.frequency,
            "is_active": schedule.is_active
        }
    )


