VALID_REPORT_TYPES = [
    "executive_summary",
    "monthly_forecast",
    "revenue_outlook",
    "management_summary"
]

VALID_FREQUENCIES = ["daily", "weekly", "monthly"]

@router.post("/schedules/create")
def create_schedule(
    payload: ScheduleCreate,
    db: Session = Depends(get_db),
    user = Depends(verify_role("manager"))
):
    if payload.report_type not in VALID_REPORT_TYPES:
        raise HTTPException(
            status_code=400,
            detail=f"Invalid report_type. Choose from: {VALID_REPORT_TYPES}"
        )

    if payload.frequency not in VALID_FREQUENCIES:
        raise HTTPException(
            status_code=400,
            detail=f"Invalid frequency. Choose from: {VALID_FREQUENCIES}"
        )

    # Compute next_run_at
    now = datetime.utcnow()
    if payload.frequency == "daily":
        next_run = now + timedelta(days=1)
    elif payload.frequency == "weekly":
        next_run = now + timedelta(weeks=1)
    else:
        next_run = now + timedelta(days=30)

    schedule = ReportSchedule(
        report_type=payload.report_type,
        frequency=payload.frequency,
        day_of_week=payload.day_of_week,
        day_of_month=payload.day_of_month,
        created_by=current_user.id,
        next_run_at=next_run
    )

    db.add(schedule)
    db.commit()
    db.refresh(schedule)

    return success_response(
        message="Report schedule created successfully!",
        data={
            "id": schedule.id,
            "report_type": schedule.report_type,
            "frequency": schedule.frequency,
            "is_active": schedule.is_active,
            "next_run_at": schedule.next_run_at
        }
    )


