@router.get("/monthly-forecast-report")
def monthly_forecast_report(
    year: Optional[int] = Query(None, description="Filter by year e.g. 2024"),
    db: Session = Depends(get_db),
    user = Depends(verify_role("all"))
):
    """
    Generates a monthly aggregated forecast + sales Excel report.
    Groups actual sales and predicted demand by month.
    """

    sales_query = db.query(
        func.date_format(Sales.sales_date, "%Y-%m").label("month"),
        func.sum(Sales.revenue).label("total_revenue"),
        func.sum(Sales.quantity_sold).label("total_qty")
    ).group_by("month")

    if year:
        sales_query = sales_query.filter(
            func.year(Sales.sales_date) == year
        )

    monthly_sales = sales_query.order_by("month").all()

    forecast_rows = db.query(ForecastResult).order_by(
        ForecastResult.forecast_date.asc()
    ).all()

    # Group forecast by month
    forecast_monthly = {}
    for f in forecast_rows:
        key = str(f.forecast_date)[:7]
        if key not in forecast_monthly:
            forecast_monthly[key] = []
        forecast_monthly[key].append(f.predicted_demand)

    # Build Excel
    data = []
    for row in monthly_sales:
        avg_forecast = None
        if row.month in forecast_monthly:
            vals = forecast_monthly[row.month]
            avg_forecast = round(sum(vals) / len(vals), 2)
        data.append({
            "Month": row.month,
            "Actual Revenue (₹)": round(float(row.total_revenue), 2),
            "Actual Quantity Sold": int(row.total_qty),
            "Avg Forecasted Demand": avg_forecast or "N/A"
        })

    df = pd.DataFrame(data)
    filename = f"monthly_forecast_{datetime.now().strftime('%Y%m%d_%H%M%S')}.xlsx"
    file_path = os.path.join(REPORTS_DIR, filename)

    with pd.ExcelWriter(file_path, engine="openpyxl") as writer:
        df.to_excel(writer, index=False, sheet_name="Monthly Forecast")
        worksheet = writer.sheets["Monthly Forecast"]
        for col in worksheet.columns:
            worksheet.column_dimensions[col[0].column_letter].width = 22

    save_report_record(db, filename, file_path, "excel", current_user.username)

    return FileResponse(
        path=file_path,
        filename=filename,
        media_type="application/vnd.openxmlformats-officedocument.spreadsheetml.sheet"
    )


